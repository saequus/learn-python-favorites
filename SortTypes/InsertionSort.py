def insertion_sort(arr):
    for i in range(1, len(arr)):
        key = arr[i]

        j = i - 1
        while j >= 0 and key < arr[j]:  # in order to make sort reverse change to key > arr[j]
            arr[j + 1] = arr[j]
            j -= 1
        arr[j + 1] = key


a = [3, 5, 1, 30, 32, 6, 34, 14, 16, 66, 48]
insertion_sort(a)
print(a)

